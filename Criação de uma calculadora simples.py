n1 = int(input("Digite um numero: "))
n2 = int(input("Digite outro numero: "))
print("----------------------------------")
#Adição
print("ADIÇÃO")
print("{} + {}".format(n1, n2))
print(n1 + n2)
print("----------------------------------")
#SUBTRAÇÃO
print("SUBTRAÇÃO")
print("{} - {}".format(n1, n2))
print(n1 - n2)
print("----------------------------------")
#MULTIPLICAÇÃO
print("MULTIPLICAÇÃO")
print("{} * {}".format(n1, n2))
print(n1 * n2)
print("----------------------------------")
#DIVISÃO
print("DIVISÃO")
print("{} / {}".format(n1, n2))
print(n1 / n2)